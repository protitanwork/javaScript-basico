function ola(nome){
    return `ola ${nome}`;
}

function somar (a, b){
    return a + b; 
}

function treinar(minutos){
    return `você treinou ${minutos}`;
}

function pesoAlvo( peso, alvo){
    return `falta ${alvo- peso} kg para o peso alvo`;
}

function motivar(nome){
    return `${nome} continue firme!`;
}

console.log(ola("Larissa"));
console.log(somar(30,40));
console.log(treinar(30));
console.log(pesoAlvo(69,75));
console.log(motivar("Sérgio"))
